# left shift : <<
# in most case we multiply with power of 2

# Right shift: >>
# in most case we divide with power of two

# padding for positive number is done with 0
# padding for negative number is dependent on compiler

print(17>>1)
print(17>>2)
print(19<<1)
print(21<<2)



